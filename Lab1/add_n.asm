ORG 0000H
LJMP MAIN

ADDER_N:
	MOV A,50h
	ANL A,#0FFh
	JZ RETURN
	MOV SP,#50h
	MOV R0, A
	MOV R1,#01h
	CLR A
	
	ADD_NEXT:
		ADD A,R1
		PUSH ACC
		INC R1
	DJNZ R0,ADD_NEXT
	
	RETURN:
	CLR A
	CLR R0
	CLR R1
	RET

INIT:
	MOV 50h, #00h
	RET


ORG 0100H
MAIN:
	MOV SP,#0C0H
	ACALL INIT
	ACALL ADDER_N
END
